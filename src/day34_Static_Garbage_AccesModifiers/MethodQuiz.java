package day34_Static_Garbage_AccesModifiers;

public class MethodQuiz {

    public static void main(String[] args) {
        int z = 10;

        switch(z){
            case  10 :
                System.out.println("Mon");

            case 11 :
                System.out.println("Tue");
            default:
                System.out.println("Fri");
        }
    }



















}
