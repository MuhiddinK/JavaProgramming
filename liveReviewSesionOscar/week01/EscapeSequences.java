package week01;

public class EscapeSequences {

    public static void main(String[] args) {

        /*  \t : tab
       \r : inserts a carriage return in the text at the
       \n : take it to the next line
       \b : inserts a backspace in the text at this point
       \f : insert form feed in the text at this point
       \\ : prints \
       \" : prints "
       \' : prints '
        */
        System.out.println("Learn\tJava\n\tthe \nHard\tWay\n\n");
        System.out.println("\tLearn Java the\"EASY WAY\" \n\t\t with OSCAR");

    }


}
